package com.cbs.dto;

import java.util.Comparator;

/**
 *
 * @author root
 */
public class AcNoByComparator implements Comparator<TdIntDetail>{
    public int compare(TdIntDetail tdIntDetailObj1, TdIntDetail tdIntDetailObj2){
        return tdIntDetailObj1.getAcno().compareTo(tdIntDetailObj2.getAcno());
    }

}
