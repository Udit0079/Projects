package com.cbs.dto;

/**
 *
 * @author Navneet
 */
public class DividendReconTempTable implements Comparable {

    String acno = "";
    String relatedAcno = "";
    private Double cramtSum = 0.0;
    

    public String getAcno() {
        return acno;
    }

    public void setAcno(String acno) {
        this.acno = acno;
    }

    public Double getCramtSum() {
        return cramtSum;
    }

    public void setCramtSum(Double cramtSum) {
        this.cramtSum = cramtSum;
    }

    public String getRelatedAcno() {
        return relatedAcno;
    }

    public void setRelatedAcno(String relatedAcno) {
        this.relatedAcno = relatedAcno;
    }
    

    public int compareTo(Object o) {
        return acno.compareTo(((DividendReconTempTable) o).acno);
    }
}
