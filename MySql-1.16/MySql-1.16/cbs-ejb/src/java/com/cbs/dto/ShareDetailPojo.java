package com.cbs.dto;

/**
 *
 * @author root
 */
public class ShareDetailPojo {
    
    long certNo;
    long shareNoFrom;
    long shareNoTo;
    long noOfShare;
    String issueDt;

    public long getCertNo() {
        return certNo;
    }

    public void setCertNo(long certNo) {
        this.certNo = certNo;
    }

    public long getShareNoFrom() {
        return shareNoFrom;
    }

    public void setShareNoFrom(long shareNoFrom) {
        this.shareNoFrom = shareNoFrom;
    }

    public long getShareNoTo() {
        return shareNoTo;
    }

    public void setShareNoTo(long shareNoTo) {
        this.shareNoTo = shareNoTo;
    }

    public long getNoOfShare() {
        return noOfShare;
    }

    public void setNoOfShare(long noOfShare) {
        this.noOfShare = noOfShare;
    }

    public String getIssueDt() {
        return issueDt;
    }

    public void setIssueDt(String issueDt) {
        this.issueDt = issueDt;
    }
    
    
}
