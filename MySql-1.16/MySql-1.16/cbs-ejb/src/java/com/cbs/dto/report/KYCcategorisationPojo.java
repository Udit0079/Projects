package com.cbs.dto.report;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 *
 * @author Athar Reza
 */
public class KYCcategorisationPojo implements Serializable{
    private String acNo;
    private String acType;
    private String custName;
    private String kycCateg;
    private BigDecimal bal;
    private BigDecimal annualIncome;
    private String orrRatingDt;

    public String getOrrRatingDt() {
        return orrRatingDt;
    }

    public void setOrrRatingDt(String orrRatingDt) {
        this.orrRatingDt = orrRatingDt;
    }
    
    public String getAcNo() {
        return acNo;
    }

    public void setAcNo(String acNo) {
        this.acNo = acNo;
    }

    public BigDecimal getAnnualIncome() {
        return annualIncome;
    }

    public void setAnnualIncome(BigDecimal annualIncome) {
        this.annualIncome = annualIncome;
    }

    public BigDecimal getBal() {
        return bal;
    }

    public void setBal(BigDecimal bal) {
        this.bal = bal;
    }

    public String getCustName() {
        return custName;
    }

    public void setCustName(String custName) {
        this.custName = custName;
    }

    public String getKycCateg() {
        return kycCateg;
    }

    public void setKycCateg(String kycCateg) {
        this.kycCateg = kycCateg;
    }

    public String getAcType() {
        return acType;
    }

    public void setAcType(String acType) {
        this.acType = acType;
    }
        
}
