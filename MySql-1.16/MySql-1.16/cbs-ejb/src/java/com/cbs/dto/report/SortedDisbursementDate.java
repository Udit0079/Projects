package com.cbs.dto.report;

import java.util.Comparator;

/**
 *
 * @author Athar Reza
 */
public class SortedDisbursementDate implements Comparator<LoanDisbursementPojo> {
    
     public int compare(LoanDisbursementPojo object1, LoanDisbursementPojo object2) {
        return object1.getLastAmtDisbDt().compareTo(object2.getLastAmtDisbDt());
    }
    
}
