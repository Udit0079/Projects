package com.cbs.pojo;

import java.math.BigDecimal;

/**
 *
 * @author Admin
 */
public class OverDueListPojo {
    
    private String acName;
    private String custName;
    private String acType;
    private BigDecimal totalDemand;
    private BigDecimal totalRecover;
    private BigDecimal oveDue;
    private BigDecimal balance;

    public String getAcName() {
        return acName;
    }

    public void setAcName(String acName) {
        this.acName = acName;
    }

    public String getCustName() {
        return custName;
    }

    public void setCustName(String custName) {
        this.custName = custName;
    }

    public BigDecimal getTotalDemand() {
        return totalDemand;
    }

    public void setTotalDemand(BigDecimal totalDemand) {
        this.totalDemand = totalDemand;
    }

    public BigDecimal getTotalRecover() {
        return totalRecover;
    }

    public void setTotalRecover(BigDecimal totalRecover) {
        this.totalRecover = totalRecover;
    }

    public BigDecimal getOveDue() {
        return oveDue;
    }

    public void setOveDue(BigDecimal oveDue) {
        this.oveDue = oveDue;
    }

    public BigDecimal getBalance() {
        return balance;
    }

    public void setBalance(BigDecimal balance) {
        this.balance = balance;
    }

    public String getAcType() {
        return acType;
    }

    public void setAcType(String acType) {
        this.acType = acType;
    }
    
   
}
