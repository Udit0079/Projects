package com.hrms.web.pojo;

import java.io.Serializable;
import java.util.Date;

/**
 *
 * @author admin
 */
public class HolidayMasterTable implements Serializable {

    private long holidayCode;
    private String holidaydate;
    private String holidayday;
    private String holidaydes;
    private Date holidayfromdt;
    private Date holidaytodt;

    public long getHolidayCode() {
        return holidayCode;
    }

    public void setHolidayCode(long holidayCode) {
        this.holidayCode = holidayCode;
    }
    
    public String getHolidaydate() {
        return holidaydate;
    }

    public void setHolidaydate(String holidaydate) {
        this.holidaydate = holidaydate;
    }

    public String getHolidayday() {
        return holidayday;
    }

    public void setHolidayday(String holidayday) {
        this.holidayday = holidayday;
    }

    public String getHolidaydes() {
        return holidaydes;
    }

    public void setHolidaydes(String holidaydes) {
        this.holidaydes = holidaydes;
    }

    public Date getHolidayfromdt() {
        return holidayfromdt;
    }

    public void setHolidayfromdt(Date holidayfromdt) {
        this.holidayfromdt = holidayfromdt;
    }

    public Date getHolidaytodt() {
        return holidaytodt;
    }

    public void setHolidaytodt(Date holidaytodt) {
        this.holidaytodt = holidaytodt;
    }
}
