package com.hrms.web.pojo;

import java.io.Serializable;

/**
 *
 * @author ROHIT KRISHNA
 */
public class InstituteDetailGrid implements Serializable {

    public String instituteCode;
    public String compCode;
    public String instituteName;
    public String instituteAdd;
    public String contactPerson;
    public String contactNum;

    public String getCompCode() {
        return compCode;
    }

    public void setCompCode(String compCode) {
        this.compCode = compCode;
    }

    public String getInstituteCode() {
        return instituteCode;
    }

    public void setInstituteCode(String instituteCode) {
        this.instituteCode = instituteCode;
    }
    

    public String getContactNum() {
        return contactNum;
    }

    public void setContactNum(String contactNum) {
        this.contactNum = contactNum;
    }

    public String getContactPerson() {
        return contactPerson;
    }

    public void setContactPerson(String contactPerson) {
        this.contactPerson = contactPerson;
    }

    public String getInstituteAdd() {
        return instituteAdd;
    }

    public void setInstituteAdd(String instituteAdd) {
        this.instituteAdd = instituteAdd;
    }

    public String getInstituteName() {
        return instituteName;
    }

    public void setInstituteName(String instituteName) {
        this.instituteName = instituteName;
    }

    
}
