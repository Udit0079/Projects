package com.cbs.dto.master;

import java.io.Serializable;

/**
 *
 * @author root
 */
public class CbsChargeDetailTO implements Serializable {

    private String chargeType;
    private String chargeName;
    private Double amount;
    private String creditHead;
    private String frequency;

    public String getChargeName() {
        return chargeName;
    }

    public void setChargeName(String chargeName) {
        this.chargeName = chargeName;
    }

    public String getChargeType() {
        return chargeType;
    }

    public void setChargeType(String chargeType) {
        this.chargeType = chargeType;
    }

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

    public String getCreditHead() {
        return creditHead;
    }

    public void setCreditHead(String creditHead) {
        this.creditHead = creditHead;
    }
    
    public String getFrequency() {
        return frequency;
    }

    public void setFrequency(String frequency) {
        this.frequency = frequency;
    }
}
