package com.cbs.dto.neftrtgs;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class ChannelReplyDto {

    @JsonProperty("UniqueChnlRefNo")
    private String UniqueChnlRefNo;
    @JsonProperty("RespId")
    private String RespId;
    @JsonProperty("UniqueBankRefNo")
    private String UniqueBankRefNo;
    @JsonProperty("RespCode")
    private String RespCode;
    @JsonProperty("AddDesc")
    private String AddDesc;
    @JsonProperty("TrnAmount")
    private String TrnAmount;

    public String getUniqueChnlRefNo() {
        return UniqueChnlRefNo;
    }

    public void setUniqueChnlRefNo(String UniqueChnlRefNo) {
        this.UniqueChnlRefNo = UniqueChnlRefNo;
    }

    public String getRespId() {
        return RespId;
    }

    public void setRespId(String RespId) {
        this.RespId = RespId;
    }

    public String getUniqueBankRefNo() {
        return UniqueBankRefNo;
    }

    public void setUniqueBankRefNo(String UniqueBankRefNo) {
        this.UniqueBankRefNo = UniqueBankRefNo;
    }

    public String getRespCode() {
        return RespCode;
    }

    public void setRespCode(String RespCode) {
        this.RespCode = RespCode;
    }

    public String getAddDesc() {
        return AddDesc;
    }

    public void setAddDesc(String AddDesc) {
        this.AddDesc = AddDesc;
    }

    public String getTrnAmount() {
        return TrnAmount;
    }

    public void setTrnAmount(String TrnAmount) {
        this.TrnAmount = TrnAmount;
    }

    @Override
    public String toString() {
        return "UniqueBankRefNo-->" + UniqueBankRefNo + "::RespCode-->" + RespCode + "::AddDesc-->"
                + AddDesc + "::TrnAmount-->" + TrnAmount;
    }
}
