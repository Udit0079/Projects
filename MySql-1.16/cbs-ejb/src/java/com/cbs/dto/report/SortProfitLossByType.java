package com.cbs.dto.report;

import java.util.Comparator;

/**
 *
 * @author root
 */
public class SortProfitLossByType implements Comparator<ProfitAndLossPojo> {

    public int compare(ProfitAndLossPojo obj1, ProfitAndLossPojo obj2) {
        return obj1.getType().compareTo(obj2.getType());
    }
}
