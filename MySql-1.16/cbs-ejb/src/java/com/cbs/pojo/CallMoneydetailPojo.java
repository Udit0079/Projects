package com.cbs.pojo;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 *
 * @author Admin
 */
public class CallMoneydetailPojo implements Serializable {

    private String ctrNo;
    private String date;
    private String period;
    private String dateOfMat;
    private BigDecimal bookVal;
    private double roi;
    private BigDecimal inttAmt;
    private BigDecimal matAmt;

    public String getCtrNo() {
        return ctrNo;
    }

    public void setCtrNo(String ctrNo) {
        this.ctrNo = ctrNo;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPeriod() {
        return period;
    }

    public void setPeriod(String period) {
        this.period = period;
    }

    public String getDateOfMat() {
        return dateOfMat;
    }

    public void setDateOfMat(String dateOfMat) {
        this.dateOfMat = dateOfMat;
    }

    public BigDecimal getBookVal() {
        return bookVal;
    }

    public void setBookVal(BigDecimal bookVal) {
        this.bookVal = bookVal;
    }

    public double getRoi() {
        return roi;
    }

    public void setRoi(double roi) {
        this.roi = roi;
    }

    public BigDecimal getInttAmt() {
        return inttAmt;
    }

    public void setInttAmt(BigDecimal inttAmt) {
        this.inttAmt = inttAmt;
    }

    public BigDecimal getMatAmt() {
        return matAmt;
    }

    public void setMatAmt(BigDecimal matAmt) {
        this.matAmt = matAmt;
    }
    
    
}
