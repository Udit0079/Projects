package com.cbs.pojo;

import java.math.BigDecimal;

/**
 *
 * @author Admin
 */
public class ExpenditureBalDayWisePojo {

    private String txnDate;
    private String glName;
    private BigDecimal expsBal;
    private String branch;
    private int openedAcno;
    private int closedAcno;
    private int netOpenAcno;
    private String reportType;

    public String getTxnDate() {
        return txnDate;
    }

    public void setTxnDate(String txnDate) {
        this.txnDate = txnDate;
    }

    public String getGlName() {
        return glName;
    }

    public void setGlName(String glName) {
        this.glName = glName;
    }

    public BigDecimal getExpsBal() {
        return expsBal;
    }

    public void setExpsBal(BigDecimal expsBal) {
        this.expsBal = expsBal;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public int getOpenedAcno() {
        return openedAcno;
    }

    public void setOpenedAcno(int openedAcno) {
        this.openedAcno = openedAcno;
    }

    public int getClosedAcno() {
        return closedAcno;
    }

    public void setClosedAcno(int closedAcno) {
        this.closedAcno = closedAcno;
    }

    public int getNetOpenAcno() {
        return netOpenAcno;
    }

    public void setNetOpenAcno(int netOpenAcno) {
        this.netOpenAcno = netOpenAcno;
    }

    public String getReportType() {
        return reportType;
    }

    public void setReportType(String reportType) {
        this.reportType = reportType;
    }
       
}
