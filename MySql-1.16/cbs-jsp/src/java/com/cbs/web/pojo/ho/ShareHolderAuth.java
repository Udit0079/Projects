public class ShareHolderAuth {
    int sNo;
    long shareNoFrom;
    long shareNoTo;
    long noOfShare;
    String issueDt;
    String issueBy;
    String auth;

    public long getNoOfShare() {
        return noOfShare;
    }

    public void setNoOfShare(long noOfShare) {
        this.noOfShare = noOfShare;
    }

    public String getAuth() {
        return auth;
    }

    public void setAuth(String auth) {
        this.auth = auth;
    }

    public String getIssueDt() {
        return issueDt;
    }

    public void setIssueDt(String issueDt) {
        this.issueDt = issueDt;
    }

    public String getIssueBy() {
        return issueBy;
    }

    public void setIssueBy(String issueBy) {
        this.issueBy = issueBy;
    }

    public long getShareNoFrom() {
        return shareNoFrom;
    }

    public void setShareNoFrom(long shareNoFrom) {
        this.shareNoFrom = shareNoFrom;
    }

    public long getShareNoTo() {
        return shareNoTo;
    }

    public void setShareNoTo(long shareNoTo) {
        this.shareNoTo = shareNoTo;
    }

    public int getsNo() {
        return sNo;
    }

    public void setsNo(int sNo) {
        this.sNo = sNo;
    }

   
}
