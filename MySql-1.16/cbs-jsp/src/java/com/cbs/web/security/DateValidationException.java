package com.cbs.web.security;

import org.springframework.security.core.AuthenticationException;

/**
 *
 * @author root
 */
public class DateValidationException extends AuthenticationException {

    public DateValidationException(String msg) {
        super(msg);
    }

    public DateValidationException(String msg, Throwable t) {
        super(msg, t);
    }

    public DateValidationException(String msg, Object extraInformation){
        super(msg, extraInformation);
    }
}
