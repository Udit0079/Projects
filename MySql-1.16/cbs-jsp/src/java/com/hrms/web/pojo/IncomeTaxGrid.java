package com.hrms.web.pojo;

/**
 *
 * @author Sudhir Kr Bisht
 */
public class IncomeTaxGrid {
    private String rangeFrom;
    private String rangeTo;
    private String salaryComponent;
    private String salaryTaxPercentage;
    private String maxAmount;

    public String getMaxAmount() {
        return maxAmount;
    }

    public void setMaxAmount(String maxAmount) {
        this.maxAmount = maxAmount;
    }

    public String getRangeFrom() {
        return rangeFrom;
    }

    public void setRangeFrom(String rangeFrom) {
        this.rangeFrom = rangeFrom;
    }

    public String getRangeTo() {
        return rangeTo;
    }

    public void setRangeTo(String rangeTo) {
        this.rangeTo = rangeTo;
    }

    public String getSalaryComponent() {
        return salaryComponent;
    }

    public void setSalaryComponent(String salaryComponent) {
        this.salaryComponent = salaryComponent;
    }

    public String getSalaryTaxPercentage() {
        return salaryTaxPercentage;
    }

    public void setSalaryTaxPercentage(String salaryTaxPercentage) {
        this.salaryTaxPercentage = salaryTaxPercentage;
    }

}
